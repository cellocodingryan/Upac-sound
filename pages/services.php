<h1>Our Services</h1>

<h2>Rigs</h2>
<div class="section">
    <h3>A-Rig</h3>
    <p>
        A-Rig is our largest setup and is best suited for large concerts and dances. It is most commonly set up in the McNeil room for UPAC Concerts, but it has also been used in the ECAV GYM, the Houston Fieldhouse and on the Commons Field.
    <h3>B-Rig</h3>
    <p>This is the most commonly requested rig, as it is suited to most events.</p>
    <p>A two hour event in the McNeil room with no soundcheck costs approximately $160. For non-union-funded organizations there is an additional $100 equipment fee. For West Hall and Academy Hall events, add an additional $30.</p>
    <h3>C-Rig / Rental Rig</h3>
    <p>This is our smallest rig, and is suitible for speaking engagements. If music (particularly bass-heavy) is the focus, B-Rig is a better choice.</p>
    <p>A two hour event in the McNeil room with no soundcheck costs approximately $80. There is an additional $80 equipment fee for non-union-funded organizations</p>
    <p>This rig can be rented for free by union-funded organizations. For other organizations the rental fee is $80.</p>
